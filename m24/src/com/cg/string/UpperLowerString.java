package com.cg.string;
public class UpperLowerString {

	public static void main(String[] args) {
		
		String s=new String("Hello Friends");//Create string
		String lower=s.toLowerCase(); //string to lower case
        System.out.println("lower Case "+lower); // print 
        
        String upper=s.toUpperCase();//string to upper
 
        System.out.println("Upper Case    "+upper);//print
        
}}